        <h2>Mon Parcours</h2>
        <p>Après sept ans d’apprentissage dans divers ateliers de théâtre, je me suis dirigée vers l’option Art dramatique au lycée d’Annecy où j’ai passé le baccalauréat. J’ai ainsi pu poursuivre mon initiation, travailler plusieurs textes dramatiques ( Molière, A. Musset, J. Racine, J.L. Lagarce, Dario Fo, N. Erdman, …), être confrontée à la mise en scène et jouer à plusieurs reprises à Bonlieu, 3ème scène nationale de France, à Annecy. Cette expérience m’a permis de développer mon goût pour la dramaturgie et m’a confortée dans mon désir de devenir comédienne professionnelle.</p>
        <p>A Paris, je suis entrée en cycle Art dramatique au Conservatoire Municipal du 17ème arrondissement. Les ateliers de danse, de chant, de commedia dell’arte, etc… qui y sont organisés me permettent d’approfondir et d’enrichir mon jeu. Je participe également à des projets professionnels tels que le projet Fosse, de Christian Boltanski au centre Pompidou à deux reprises, la réalisation d’un court métrage et de ma bande démo, pour me préparer aux attentes et aux besoins du métier.</p>
        <p>Cette formation essentiellement théâtrale m’a conduite vers le milieu du cinéma et j’ai participé aux tournages de différents clips vidéos et courts métrages.</p>
